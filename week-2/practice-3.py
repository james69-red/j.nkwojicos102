# Define the main list with mixed data types
my_list = ['Anaconda', 786, 2.23, 'Jupyter', 70.2]
# Define a shorter list
short_list = [321, 'Python']
# Print the entire list
print(my_list)  
# Print the first element of the list
print(my_list[0])  
# Print elements from 2nd to 3rd (index 1 to 2)
print(my_list[1:3])  # Prints elements starting from 2nd till 3rd
# Print elements starting from the 3rd element to the end (index 2 onward)
print(my_list[2:])
# Print the short list two times
print(short_list * 2)
# Concatenate the two lists and print
print(my_list + short_list)
